#创建disease知识图谱函数
def create_disease_knowledge_graph(graph, diseases):
    tx = graph.begin()  # 手动开始事务
    try:
        for disease_name, data in diseases.items():
            pathogen_str = data.get("pathogen", "")
            symptoms = data.get("symptoms", [])
            treatments = data.get("treatments", [])

            # 创建或合并疾病节点
            disease_node = Node("Disease", name=disease_name)
            tx.merge(disease_node, "Disease", "name")

            #创造或合并病原节点
            if pathogen_str:
                pathogen_node = Node("Pathogen", description=pathogen_str)
                tx.merge(pathogen_node, "Pathogen", "description")

                #创造疾病和病原之间的关系
                pathogen_to_disease=Relationship(disease_node, "Caused_By", pathogen_node)
                pathogen_to_disease['name'] = "是因为"
                tx.merge(pathogen_to_disease)

            # 创建或合并症状节点
            for symptom in symptoms:
                symptom_node = Node("Symptom", description=symptom)
                tx.merge(symptom_node, "Symptom", "description")

                # 创建疾病和症状之间的关系
                disease_to_symptom = Relationship(disease_node, "Has_Symptom", symptom_node)
                disease_to_symptom['name'] = "会引起"
                tx.merge(disease_to_symptom)

                #创造症状和疾病之间的关系
                symptom_to_disease = Relationship(symptom_node, "Can_Be", disease_node)
                symptom_to_disease['name'] = "可能是"
                tx.merge(symptom_to_disease)

            # 创建或合并防治措施节点
            for treatment in treatments:
                treatment_node = Node("Treatment", description=treatment)
                tx.merge(treatment_node, "Treatment", "description")

                # 创建疾病和防治措施之间的关系
                disease_to_treatment = Relationship(disease_node, "Treated_By", treatment_node)
                disease_to_treatment['name'] = "可以"
                tx.merge(disease_to_treatment)

        graph.commit(tx)  # 提交事务
    except Exception as e:
        graph.rollback(tx)  # 出现异常时回滚事务
        print(f"An error occurred: {e}")

#创建problem知识图谱函数
def create_problem_knowledge_graph(graph, data):
    tx = graph.begin()  # 手动开始事务
    try:
        for aspect, problems in data.items():
            # 创建或合并方面节点
            aspect_node = Node("Aspect", description=aspect)
            tx.merge(aspect_node, "Aspect", "description")

            for problem_desc, solutions in problems.items():
                # 创建或合并问题节点
                problem_node = Node("Problem", description=problem_desc)
                tx.merge(problem_node, "Problem", "description")

                # 创建方面与问题的关系
                aspect_to_problem = Relationship(aspect_node, "Has_Problem", problem_node)
                tx.merge(aspect_to_problem)

                for solution in solutions["solution"]:
                    # 创建或合并解决方案节点
                    solution_node = Node("Solution", description=solution)
                    tx.merge(solution_node, "Solution", "description")

                    # 创建问题与解决方案的关系
                    problem_to_solution = Relationship(problem_node, "Solved_By", solution_node)
                    tx.merge(problem_to_solution)

        graph.commit(tx)  # 提交事务
    except Exception as e:
        graph.rollback(tx)  # 出现异常时回滚事务
        print(f"An error occurred: {e}")


